
public class Triangle {
	private int Base;
	private int per;
	private int hye;
	
	public Triangle()
	{
		Base=0;
		per=0;
		hye=0;
	}
	public void setBase(int b)
	{
		Base=b;
		
	}
	public int getBase()
	{
		return Base;
	}
	public void setper(int p)
	{
		Base=p;
		
	}
	public int getper()
	{
		return per;
	}
	public void sethye(int h)
	{
		hye=h;
		
	}
	public int gethye()
	{
		return hye;
	}
	

}
